package pe.com.sigamm.modelo;

public class ServicioOtrosDetalle {

	int codigoServicioOtros;
	int codigoServicioDetalle;
    int codigoBanio;
    int rangoInicio;
    int rangoFin;
    
    String rango;
    String nombreBanio;
    String importe;
    String nombreDetalle;
    
	public int getCodigoServicioOtros() {
		return codigoServicioOtros;
	}
	public void setCodigoServicioOtros(int codigoServicioOtros) {
		this.codigoServicioOtros = codigoServicioOtros;
	}
	public int getCodigoServicioDetalle() {
		return codigoServicioDetalle;
	}
	public void setCodigoServicioDetalle(int codigoServicioDetalle) {
		this.codigoServicioDetalle = codigoServicioDetalle;
	}
	public int getCodigoBanio() {
		return codigoBanio;
	}
	public void setCodigoBanio(int codigoBanio) {
		this.codigoBanio = codigoBanio;
	}
	public int getRangoInicio() {
		return rangoInicio;
	}
	public void setRangoInicio(int rangoInicio) {
		this.rangoInicio = rangoInicio;
	}
	public int getRangoFin() {
		return rangoFin;
	}
	public void setRangoFin(int rangoFin) {
		this.rangoFin = rangoFin;
	}
	public String getRango() {
		return rango;
	}
	public void setRango(String rango) {
		this.rango = rango;
	}
	public String getNombreBanio() {
		return nombreBanio;
	}
	public void setNombreBanio(String nombreBanio) {
		this.nombreBanio = nombreBanio;
	}
	public String getImporte() {
		return importe;
	}
	public void setImporte(String importe) {
		this.importe = importe;
	}
	public String getNombreDetalle() {
		return nombreDetalle;
	}
	public void setNombreDetalle(String nombreDetalle) {
		this.nombreDetalle = nombreDetalle;
	}
                
}
