package pe.com.sigamm.modelo;

public class FacturacionCabecera {

	int codigoFacturacionCab;
	int codigoUsuario;
	String serie;
	String secuencia;
	int estado;
	String facturacionDetalle;
	int codigoSocio;
	
	String fechaCreacion;
	String nombresFull;
	String nroPuesto;
	String nombreGiro;
	String nombreSector;
	String comprobante;
	
	public int getCodigoFacturacionCab() {
		return codigoFacturacionCab;
	}
	public void setCodigoFacturacionCab(int codigoFacturacionCab) {
		this.codigoFacturacionCab = codigoFacturacionCab;
	}
	public int getCodigoUsuario() {
		return codigoUsuario;
	}
	public void setCodigoUsuario(int codigoUsuario) {
		this.codigoUsuario = codigoUsuario;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public String getSecuencia() {
		return secuencia;
	}
	public void setSecuencia(String secuencia) {
		this.secuencia = secuencia;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	public String getFacturacionDetalle() {
		return facturacionDetalle;
	}
	public void setFacturacionDetalle(String facturacionDetalle) {
		this.facturacionDetalle = facturacionDetalle;
	}
	public int getCodigoSocio() {
		return codigoSocio;
	}
	public void setCodigoSocio(int codigoSocio) {
		this.codigoSocio = codigoSocio;
	}
	public String getFechaCreacion() {
		return fechaCreacion;
	}
	public void setFechaCreacion(String fechaCreacion) {
		this.fechaCreacion = fechaCreacion;
	}
	public String getNombresFull() {
		return nombresFull;
	}
	public void setNombresFull(String nombresFull) {
		this.nombresFull = nombresFull;
	}
	public String getNroPuesto() {
		return nroPuesto;
	}
	public void setNroPuesto(String nroPuesto) {
		this.nroPuesto = nroPuesto;
	}
	public String getNombreGiro() {
		return nombreGiro;
	}
	public void setNombreGiro(String nombreGiro) {
		this.nombreGiro = nombreGiro;
	}
	public String getNombreSector() {
		return nombreSector;
	}
	public void setNombreSector(String nombreSector) {
		this.nombreSector = nombreSector;
	}
	public String getComprobante() {
		return comprobante;
	}
	public void setComprobante(String comprobante) {
		this.comprobante = comprobante;
	}
					
}
