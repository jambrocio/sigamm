package pe.com.sigamm.modelo;

public class ServicioOtrosCabecera {
	
	int codigoUsuario;
	int codigoServicio;
	int codigoSocio;
	String dniResponsable;
	String nombreResponsable;
	String servicioOtrosDetalle;
	
	public int getCodigoUsuario() {
		return codigoUsuario;
	}
	public void setCodigoUsuario(int codigoUsuario) {
		this.codigoUsuario = codigoUsuario;
	}
	public int getCodigoServicio() {
		return codigoServicio;
	}
	public void setCodigoServicio(int codigoServicio) {
		this.codigoServicio = codigoServicio;
	}
	public int getCodigoSocio() {
		return codigoSocio;
	}
	public void setCodigoSocio(int codigoSocio) {
		this.codigoSocio = codigoSocio;
	}
	public String getDniResponsable() {
		return dniResponsable;
	}
	public void setDniResponsable(String dniResponsable) {
		this.dniResponsable = dniResponsable;
	}
	public String getNombreResponsable() {
		return nombreResponsable;
	}
	public void setNombreResponsable(String nombreResponsable) {
		this.nombreResponsable = nombreResponsable;
	}
	public String getServicioOtrosDetalle() {
		return servicioOtrosDetalle;
	}
	public void setServicioOtrosDetalle(String servicioOtrosDetalle) {
		this.servicioOtrosDetalle = servicioOtrosDetalle;
	} 
	    
}
