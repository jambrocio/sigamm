package pe.com.sigamm.modelo;

public class MenuPrincipal {
	
	int codigoMenu;
	int codigoNiveles;
	String descMenu;
	int ordenMenu;
	String descSubMenu;
	int ordenSubMenu;
	String link;
	int nivel;
	int subMenuAsignado;
	
	public int getCodigoMenu() {
		return codigoMenu;
	}
	public void setCodigoMenu(int codigoMenu) {
		this.codigoMenu = codigoMenu;
	}
	public int getCodigoNiveles() {
		return codigoNiveles;
	}
	public void setCodigoNiveles(int codigoNiveles) {
		this.codigoNiveles = codigoNiveles;
	}
	public String getDescMenu() {
		return descMenu;
	}
	public void setDescMenu(String descMenu) {
		this.descMenu = descMenu;
	}
	public int getOrdenMenu() {
		return ordenMenu;
	}
	public void setOrdenMenu(int ordenMenu) {
		this.ordenMenu = ordenMenu;
	}
	public String getDescSubMenu() {
		return descSubMenu;
	}
	public void setDescSubMenu(String descSubMenu) {
		this.descSubMenu = descSubMenu;
	}
	public int getOrdenSubMenu() {
		return ordenSubMenu;
	}
	public void setOrdenSubMenu(int ordenSubMenu) {
		this.ordenSubMenu = ordenSubMenu;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	public int getSubMenuAsignado() {
		return subMenuAsignado;
	}
	public void setSubMenuAsignado(int subMenuAsignado) {
		this.subMenuAsignado = subMenuAsignado;
	}
		
}
